    private void initBackEnd() {
        File input = new File(UIGlobals.configFile);
        if (input.exists() && input.isFile()) {
            operationMap = BackEndUtils.getOperationMap(UIGlobals.configFile);
            loadOperations();
        } else {
            System.err.println("Failed to load default config file : " + UIGlobals.configFile);
        }

        input = new File(UIGlobals.dataFile);
        if (input.exists() && input.isFile()) {
            dataList = BackEndUtils.getDataList(UIGlobals.dataFile);
            loadDataTable();
        } else {
            System.err.println("Failed to load default data file : " + UIGlobals.dataFile);
        }
    }

    private void loadOperations() {
        operationsComboBox.removeAllItems();
        for (String key : operationMap.keySet()) {
            operationsComboBox.addItem(key);
        }
    }

    private void loadDataTable() {
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.addColumn("#", noColAdd());

        for (List list : dataList) {
            Vector temp = new Vector(list);
            temp.remove(0);
            tableModel.addColumn(list.get(0), temp);
        }
        dataTable.setModel(tableModel);
    }

    private Vector noColAdd() {
        int maxCount = 0;
        for (List list : dataList) {
            if (maxCount < list.size()) {
                maxCount = list.size();
            }
        }
        Vector nos = new Vector();
        for (int i = 1; i < maxCount; i++) {
            nos.add(i);
        }
        return nos;
    }

