    private void loadOperationList() {
        String key = (String) operationsComboBox.getSelectedItem();
        if (key != null && operationMap != null) {
            DefaultListModel model = new DefaultListModel();
            for (String s : operationMap.get(key)) {
                model.addElement(s);
            }
            opList.setModel(model);
        }
    }

