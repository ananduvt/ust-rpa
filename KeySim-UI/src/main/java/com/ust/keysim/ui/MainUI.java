    private void startKeySim() {
        new Thread() {
            public void run() {
                int start = (int) startSpinner.getValue();
                int stop = (int) stopSpinner.getValue();
                List<String> ops = operationMap.get(operationsComboBox.getSelectedItem());
                List<List<String>> data = dataList;
                Map<String, List<String>> doMap = new HashMap();
                if (start > stop) {
                    showError();
                } else {
                    startButton.setEnabled(false);
                    stopButton.setEnabled(true);
                    keySimManager = new KeySimManager(start, stop, ops, dataList, doMap, true);
                    keySimManager.start();

                    try {
                        Thread.sleep(100);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(MainUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    monitor();
                }
            }
        }.start();
    }

    private void monitor() {
        Thread monitor = new Thread() {
            public void run() {
                while (!(keySimManager.getStatus() == Thread.State.TERMINATED)) {

                    statusLabel.setText("Running");
                    countLabel.setText(keySimManager.getCount());
                    operationLabel.setText(UiUtils.textFormat1(keySimManager.getCurrentOp()));
                    try {
                        Thread.sleep(100);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(MainUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                statusLabel.setText("Terminated");

                stopButton.setEnabled(false);
                startButton.setEnabled(true);
            }
        };
        monitor.run();
    }

