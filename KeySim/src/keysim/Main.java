    private void configLoad()
    {
        
        Loader.loadConfig();
        curntOps.clear();
        for(ArrayList<String> list : Loader.getOperations())
        {
            if(list.get(0).equals(opSelect.getSelectedItem())){
                output.setText("Operation : "+list.get(0).toUpperCase()+ "\n");
                
               for(int i=1;i<list.size();i++)
                {
                output.append("\n");
                String op=list.get(i);
                output.append(op);
                
                }
               curntOps.addAll(list);
               curntOps.remove(0);
            }
            
        }
    }

