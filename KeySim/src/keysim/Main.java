    private void startActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startActionPerformed
        // TODO add your handling code here:
        
        for(String s : curntOps)
        {
            if(s.contains("compnt"))
            {
                if(Loader.getComponents()==null)
                {
                    excelLoad=false;
                }
            }
        }
        if(Loader.getComponents()!=null)
        {
            excelLoad=true;
        }
        if(excelLoad)
        {
         try {
            robot=new Robot();
            robot.keyPress(KeyEvent.VK_ALT);
            robot.keyPress(KeyEvent.VK_TAB);
            robot.delay(50);
            robot.keyRelease(KeyEvent.VK_ALT);
            robot.keyRelease(KeyEvent.VK_TAB);               
        } catch (AWTException ex) {
            Logger.getLogger(Test.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(keySimObj!=null)
        {
         if(keySimObj.getState().equals(Thread.State.RUNNABLE))
         {
             keySimObj.stop();
             System.out.println("the thread terminated");
         }
        }
        
        keySimObj=new KeySimulator();
        keySimObj.setStart(Integer.parseInt(startIndex.getText()));
        keySimObj.setLimit(Integer.parseInt(limitIndex.getText()));
        keySimObj.setCurntOps(curntOps);
        keySimObj.setComponents(Loader.getComponents());
        
        keySimObj.start();
        
        
       count=new Thread(){
          public void run()
          {
              while(true)
              {
              CountLab.setText(String.valueOf(keySimObj.getCount()));
             
              }
          }
        };
        count.start();
        
        OpDone=new Thread(){
            public void run()
            {
                while(true)
                {
                    OpLab.setText(keySimObj.getOpr());
                }
            }
                   
        };
        OpDone.start(); 
        }
        else
        {
            output.setText("PLEASE LOAD EXCEL FILE\nOPERATIOINS REQUIRES\n COMPONONT LIST ");
        }
    }//GEN-LAST:event_startActionPerformed

