    private static void loadPosition() {
        String data = "";
        for (Click click : position.getClickList()) {
            data = data + click.getName() + " : " + "(" + click.getX() + "," + click.getY() + ")" + "\n";
        }
        for (Rect rect : position.getRectList()) {
            data = data + rect.getName() + " : " + "(" + rect.getPressX() + "," + rect.getPressY() + ")" + " , "
                    + "(" + rect.getReleaseX() + "," + rect.getReleaseY() + ")" + "\n";
        }

        ouputView.setText(ouputView.getText() + data);
        obj.setMinimumSize(new Dimension(420, 400));
        obj.setSize(420, 400);
        obj.setLocation((int) (Toolkit.getDefaultToolkit().getScreenSize().getWidth() / 1.5),
                (int) (Toolkit.getDefaultToolkit().getScreenSize().getHeight() / 3.5));
    }

