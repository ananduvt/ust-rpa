    private void showWindowTitles() {

        new Thread() {
            @Override
            public void run() {
                String currentWindow = "";
                String oldWindow = "";
                while (checkWindow) {

                    while (currentWindow.equals(oldWindow)) {

                        currentWindow = WindowUtil.getWindowTitle();
                        try {
                            Thread.sleep(100);
                        } catch (InterruptedException ex) {
                            Logger.getLogger(WindowTitleFinder.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    oldWindow = currentWindow;
                    if (!currentWindow.equals("")) {
                        ouputView.setText(ouputView.getText() + currentWindow + "\n");
                    }
                }
            }
        }.start();

    }

